# If Statement
var = 7
if var >= 5
    puts "7 is greater than 5"
end

puts "Working" if true
puts "Not working" if false


# If-Else Statement
age = 22
if age >= 18
    puts "Eligible for voting"
else
    puts "Not eligible for voting"
end



# Elsif Statement
x = 7
if x < 5
    puts "Value is less than 5"
elsif x > 5
    puts "Value is greater than 5"
else
    puts "Value is 5."
end



# Ternary Statement
x = 5
puts x != 5 ? "x is not 5." : "x is 5."