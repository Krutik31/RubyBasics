var = 3112

# Converting into str
puts var.to_s
puts var.to_s.class

# Converting into float
puts var.to_f
puts var.to_f.class

# Converting into Integer
puts "3112".to_i
puts "3112".to_i.class

# Converting into symbol
puts "3112".to_sym
puts "3112".to_sym.class

# sum of two strings that have floating values
puts "1.2".to_f + "2.8".to_f

# Converting array into string
puts [1,2,3].to_s
puts [1,2,3].to_s.class

# Symbol into string
puts :krutik.to_s
puts :krutik.to_s.class